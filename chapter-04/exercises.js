// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

//reverse the array in place
function reverseArrayInPlace(array) {
  let start = 0;
  let end = array.length - 1;
  
  while (start < end) {
      //change elements at start and end indexes
      const temp = array[start];
      array[start] = array[end];
      array[end] = temp;
      
      //move indexes towards the center
      start++;
      end--;
  }
}

