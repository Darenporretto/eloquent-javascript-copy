function min(a, b) { //find minimum of 2 args
  //compare the values and return the smaller one
  //use conditional ternary operator to check for smaller arg and return the value
  return a < b ? a : b;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  //convert neg nums to positive
  num = Math.abs(num);

  //base case
  if (num === 0) {
    return true;
  }
  if (num === 1) {
    return false;
  }
  //recur case
  return isEven(num - 2);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  let count = 0; //start the counter
  for (let i = 0; i < str.length; i++) {
    if (str[i] === char) {
      count++; //increment the counter if found a match
    }
  }
  return count; 
  //return the total char couunt
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  //use countChars to count Bs
  return countChars(str, 'B');
}


